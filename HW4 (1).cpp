#include <iostream>

using namespace std;

int main()
{
    float x, y;
    cout << "Please enter value for x: ";
    cin >> x;
    cout << "Now enter value for y: ";
    cin >> y;

    cout << "x + y = " << x + y << "\n";
    cout << "x - y = " << x - y << "\n";
    cout << "x * y = " << x * y << "\n";
    cout << "x / y = " << x / y << "\n";
}
